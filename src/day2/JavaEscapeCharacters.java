package day2;

public class JavaEscapeCharacters {

    public static void main(String[] args) {
        // new line
        System.out.println( "Hello \nhi!" );

        // tab
        System.out.println( "\tHello" );

        // quote using escape character
        System.out.println( " \" " );

        // backslash
        System.out.println( " \\ " );
    }

}
